package extraBean;

public class studenteditBean {
	private String phone;//手机号码
	private String qq;//QQ号
	private String company;//实习单位
	private String province;//所在省
	private String city;//所在市
	private String contactName;//单位联系人
	private String contactPhone;//联系人电话
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getQq() {
		return qq;
	}
	public void setQq(String qq) {
		this.qq = qq;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getContactName() {
		return contactName;
	}
	public void setContactName(String contaceName) {
		this.contactName = contaceName;
	}
	public String getContactPhone() {
		return contactPhone;
	}
	public void setContactPhone(String contancePhone) {
		this.contactPhone = contancePhone;
	}
}
